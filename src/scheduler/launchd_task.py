def _print_instructions(sh_path: Path, plist_path: Path, log_dir: Path) -> None:
    """列印每日排程安裝說明。"""
    print(f"""
{"=" * 65}
macOS LaunchAgent 安裝說明（每日 morning-routine）
{"=" * 65}

【安裝】

  cp "{plist_path}" ~/Library/LaunchAgents/
  launchctl load ~/Library/LaunchAgents/com.taiwan-quant.daily-sync.plist

【驗證】

  launchctl list | grep taiwan-quant

【手動執行測試】

  launchctl start com.taiwan-quant.daily-sync

【移除】

  launchctl unload ~/Library/LaunchAgents/com.taiwan-quant.daily-sync.plist
  rm ~/Library/LaunchAgents/com.taiwan-quant.daily-sync.plist

排程時間：每日 23:00（morning-routine：Step 0~15+8b + Discord 摘要）
日誌位置：{log_dir}/morning_routine.log
{"=" * 65}
""")


